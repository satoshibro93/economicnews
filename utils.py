def rephrase_text(text):
    replacements = {
        "впервые": "впервые в истории",
        "аналитики отмечают": "эксперты подчеркивают",
        "сохранит ключевую процентную ставку": "оставит ставку без изменений",
        "фундаментальное требование": "основное условие"
    }
    for old, new in replacements.items():
        text = text.replace(old, new)
    return text